package com.Training;

public class Looping_Statement {
	public static void main(String[] args) {
		int a =1;
		System.out.println("**while loop**");
		while (a<10) {
			System.out.println(a);
			a++;
		}
		int b =3;
	System.out.println("**Do while**");	
		do {
		b++;
		System.out.println(b);
	} while (b<10);
	}

}
